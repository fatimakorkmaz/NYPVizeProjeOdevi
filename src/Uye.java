public class Uye {
    private String adi, soyadi, email;

    public Uye(String adi, String soyadi, String email) {
        this.adi = adi;
        this.soyadi = soyadi;
        this.email = email;
    }

    public String getAdi() {
        return adi;
    }

    public String getSoyadi() {
        return soyadi;
    }

    public String getEmail() {
        return email;
    }
}
